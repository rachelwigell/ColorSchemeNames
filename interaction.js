function hexToRgb(hex) {
  var result = /^#?([A-F\d]{2})([A-F\d]{2})([A-F\d]{2})$/i.exec(hex);
  return result ? {
      'red': parseInt(result[1], 16),
      'green': parseInt(result[2], 16),
      'blue': parseInt(result[3], 16)
  } : null;
}

function think(){
	for(var hex of choice_queue){
		var rgb = hexToRgb(hex);
		var close_colors = find_close_colors(rgb, 50.0, 1.2, 5);
		var substrings = {};
		for(var name in close_colors){
			var diff = close_colors[name];
			merge_objs(substrings, score_substrings_of_string(name, diff, close_colors));
			merge_objs(syllables, remove_substrings(substrings));
		}
	}
	calculated_name = randomize(syllables, 0);
	setTimeout(done_thinking, 2000);
}

function randomize(blocks, tries){
	var word = "";
	for(var block in blocks){
		var score = blocks[block];
		var rand = Math.random() * (Object.keys(blocks).length * 10) + score;
		if(rand < 50){
			word += block;
		}
	}
	if(word == "" && tries < 5){
		return randomize(blocks, tries+1);
	}
	else if(tries >= 5){
		return "I got nothing. Sorry!";
	}
	else{
		return word;
	}
}

function remove_substrings(mapping){
	uniques = {}
	for(var i in mapping){
		var is_substring = false;
		for(var j in mapping){
			if(j.includes(i) && i != j){
				is_substring = true;
			}
		}
		if(!is_substring){
			uniques[i] = mapping[i];
		}
	}
	return uniques;
}

function merge_objs(master_obj, sub_obj){
	for(var string in sub_obj){
		var score = sub_obj[string];
		master_score = master_obj[string];
		if(master_score != null){
			master_obj[string] = Math.min(master_score, score);
		}
		else{
			master_obj[string] = score;
		}
	}
	return master_obj;
}

function score_substrings_of_string(string, diff, close_colors){
	var scores = {};
	var substring_length = 3;
	var results_length = 1;
	while(results_length > 0){
		results = score_substring_of_length(string, diff, close_colors, substring_length);
		results_length = Object.keys(results).length;
		merge_objs(scores, results);
		substring_length++;
	}
	return scores;
}

function score_substring_of_length(string, diff, close_colors, substring_length){
	scores = {}
	for(var i = 0; i <= string.length-substring_length; i++){
		var current_score = diff;
		var add_substring = false
		substring = string.substring(i, i+substring_length);
		for(var name in close_colors){
			if(name != string && name.includes(substring)){
				current_score -= 5*substring_length;
				add_substring = true;
			}
		}
		if(add_substring){
			scores[substring] = current_score;
		}
	}
	return scores;
}

function find_close_colors(rgb, tolerance, ramp_factor, min_colors){
	var close_colors = {};
	for(var line of trainer_colors){
		diff = color_diff(rgb, line);
		if(diff <= tolerance){
			close_colors[line['name'].toLowerCase()] = diff;
		}
	}
	if(Object.keys(close_colors).length < min_colors){
		return find_close_colors(rgb, tolerance*ramp_factor, ramp_factor, min_colors);
	}
	else{
		return close_colors;
	}
}

function color_diff(color_1, color_2){
	var diff = Math.abs(color_2['red'] - color_1['red']);
	diff += Math.abs(color_2['green'] - color_1['green']);
	diff += Math.abs(color_2['blue'] - color_1['blue']);
	return diff
}

