$('#submit').click(function(){
	var choice_num = choice_queue.length;
	if(choice_num<5){
		var processing = Processing.getInstanceById('processing');
		var current_color = processing.getCurrentColor();
		choice_queue.push(current_color);
		$('#' + choice_num).css("background-color", current_color);
	}
})

$('.choice').click(function(){
	if(choosing){
		var num = this.id;
		choice_queue.splice(num, 1);
		color_queue();
	}
})

function color_queue(){
	for(var i = 0 ; i < choice_queue.length; i++){
		$('#' + i).css("background-color", choice_queue[i]);
	}
	for(var i = choice_queue.length; i < 5; i++){
		$('#' + i).css("background-color", 'white');
	}
}

function update_button_text(button_name, text){
	$('#'+button_name).text(text)
}

